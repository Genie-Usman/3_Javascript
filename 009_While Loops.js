// There are 2 types of While Loops
// 1.While Loop
// 2.Do-While Loop

// --> While Loop

// --> A Sample Program To Print Numbers through While Loop

let f = prompt("Enter Your Number");
let i = 0;
while (i<=f){
    console.log(alert(i));
    i++;
}

// --> A program to find even numbers through While Loop

let n = 0;
while (n<=100){
    if(n%2==0)
    console.log(n)
n++;
}

