// Some Console Object Methods are:

// 1. Console.Log
Syntax : console.log("Hello World");

// 2. Console.Error
Syntax : console.error("Error");

// 3. Console.Warning
Syntax : console.warn("Warning");

// 4. Console.Table
object = {a : 2 , b : 3, c : 3};
Syntax : console.table(object);

// 5. Console.Info
Syntax : console.info("Info");

// 6. Console.Clear
Syntax : console.clear("Clear");

// 7. Console.Assert
Syntax : console.assert("Assert");

// 8. Console.Time
Syntax : console.time("Time");

// 9. Console.TimeEnd
Syntax : console.timeEnd("TimeEnd");